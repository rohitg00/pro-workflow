#!/usr/bin/env node
const fs = require('fs');
const path = require('path');

const PROJECT_ROOT = path.resolve(__dirname, '..');

function parseFrontmatter(content) {
  const lines = content.split('\n');
  if (lines[0]?.trim() !== '---') return [null, content];
  const endIdx = lines.slice(1).findIndex((l) => l.trim() === '---');
  if (endIdx === -1) return [null, content];
  const fm = {};
  for (let i = 1; i <= endIdx; i++) {
    const line = lines[i];
    const match = line.match(/^(\w[\w-]*)\s*:\s*(.*)$/);
    if (!match) continue;
    const [, key, rawValue] = match;
    const value = rawValue.trim().replace(/^["']|["']$/g, '');
    if (value === 'true') fm[key] = true;
    else if (value === 'false') fm[key] = false;
    else fm[key] = value;
  }
  const body = lines.slice(endIdx + 2).join('\n').trim();
  return [fm, body];
}

function loadRules(rulesDir) {
  const rules = [];
  if (!fs.existsSync(rulesDir)) return rules;

  const files = fs.readdirSync(rulesDir).filter(
    (f) => f.endsWith('.mdc') || f.endsWith('.md')
  );

  for (const file of files) {
    const content = fs.readFileSync(path.join(rulesDir, file), 'utf-8');
    const [fm, body] = parseFrontmatter(content);

    if (fm) {
      rules.push({
        file,
        description: fm.description || path.basename(file, path.extname(file)),
        alwaysApply: fm.alwaysApply !== false,
        globs: fm.globs || null,
        body,
      });
    } else {
      rules.push({
        file,
        description: path.basename(file, path.extname(file)),
        alwaysApply: true,
        globs: null,
        body: content.trim(),
      });
    }
  }

  return rules;
}

function buildAgentsMd(rules) {
  const sections = [];
  sections.push('# Pro-Workflow Rules for OpenCode\n');
  sections.push('> Auto-generated from `rules/` by `build-opencode-rules.js`\n');

  const alwaysApply = rules.filter((r) => r.alwaysApply);
  const conditional = rules.filter((r) => !r.alwaysApply);

  if (alwaysApply.length > 0) {
    sections.push('## Global Rules (Always Apply)\n');
    for (const rule of alwaysApply) {
      sections.push(`### ${rule.description}\n`);
      sections.push(rule.body);
      sections.push('');
    }
  }

  if (conditional.length > 0) {
    sections.push('## Conditional Rules\n');
    for (const rule of conditional) {
      sections.push(`### ${rule.description}\n`);
      if (rule.globs) {
        sections.push(
          `<!-- OpenCode note: This rule applies to files matching: \`${rule.globs}\`. ` +
          `OpenCode does not natively support glob-based rule scoping. ` +
          `Consider adding equivalent patterns to \`opencode.json\` \`instructions\` field. -->\n`
        );
      }
      sections.push(rule.body);
      sections.push('');
    }
  }

  sections.push('## Conversion Notes\n');
  sections.push(
    '<!-- Rules with `.mdc`-specific glob patterns may not be automatically enforced by OpenCode. ' +
    'Use the `instructions` array in `opencode.json` to configure file-scoped rules. ' +
    'Generated by pro-workflow setup utility. -->\n'
  );

  return sections.join('\n');
}

function main() {
  const rulesDir = path.join(PROJECT_ROOT, 'rules');
  const targetPath =
    process.argv[2] || path.join(PROJECT_ROOT, '.opencode', 'AGENTS.md');

  const rules = loadRules(rulesDir);
  if (rules.length === 0) {
    console.error('No rules found in', rulesDir);
    process.exit(1);
  }

  const content = buildAgentsMd(rules);
  const targetDir = path.dirname(targetPath);
  if (!fs.existsSync(targetDir)) fs.mkdirSync(targetDir, { recursive: true });
  fs.writeFileSync(targetPath, content);

  const alwaysCount = rules.filter((r) => r.alwaysApply).length;
  const condCount = rules.filter((r) => !r.alwaysApply).length;
  console.log(
    `Rules merged: ${rules.length} total (${alwaysCount} always, ${condCount} conditional)`
  );
}

main();
